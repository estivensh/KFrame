package io.github.estivensh.kframe.info

/**
 * Represents the different platforms that a target application can be built for.
 */
enum class TargetPlatform {
    /** Android platform. */
    ANDROID,

    /** iOS platform. */
    IOS,

    /** macOS platform. */
    MACOS,

    /** Windows platform. */
    WINDOWS,

    /** Linux platform. */
    LINUX
}
